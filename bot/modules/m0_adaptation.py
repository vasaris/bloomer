def adaptation_day(arrived: dt.date | None, today: dt.date | None = None) -> int | None:
    """Номер дня адаптации (1-based) или None, если дата приезда не задана."""
    if arrived is None:
        return None
    today = today or dt.date.today()
    return (today - arrived).days + 1


def day_card(n: int) -> str:
    """Текст карточки на день n. После 21 — фаза «дома»."""
    if n in _DAYS:
        return _DAYS[n]
    if n > ADAPT_LEN:
        return (
            "🏡 Фаза «дома» (после 3 недель по 3-3-3). Адаптация пройдена — "
            "наращиваем тренинг, нюховую работу и социализацию в комфортном темпе."
        )
    return _DAYS.get(max(1, n), _DAYS[1])


@router.message(Command("arrived"))
async def cmd_arrived(message: Message, settings) -> None:
    """/arrived [ГГГГ-ММ-ДД] — задать дату приезда (по умолчанию сегодня)."""
    parts = (message.text or "").split(maxsplit=1)
    try:
        day = dt.date.fromisoformat(parts[1].strip()) if len(parts) > 1 else dt.date.today()
    except ValueError:
        await message.answer("Формат даты: /arrived 2026-06-01 (или без аргумента — сегодня).")
        return
    conn = await db.connect(settings.db_path)
    try:
        await db.set_arrived(conn, day)
    finally:
        await conn.close()
    await message.answer(
        f"Дата приезда: {day.isoformat()}. Считаю день адаптации от неё. "
        f"Сегодня — день {adaptation_day(day) or 1}."
    )


@router.message(Command("adaptation"))
async def cmd_adaptation(message: Message, settings) -> None:
    """/adaptation — карточка адаптации на сегодня."""
    conn = await db.connect(settings.db_path)
    try:
        arrived = await db.get_arrived(conn)
    finally:
        await conn.close()

    if arrived is None:
        await message.answer(
            "Сначала укажи дату приезда Блумера: /arrived ГГГГ-ММ-ДД "
            "(или просто /arrived в день приезда)."
        )
        return

    n = adaptation_day(arrived)
    await message.answer(f"День {n} из {ADAPT_LEN}\n\n{day_card(n)}")
